package entity;


public class Video {

  private int videoId;
  private String videoName;
  private String videoAdreess;
  private int videoConsume;
  private String videoType;


  public int getVideoId() {
    return videoId;
  }

  public void setVideoId(int videoId) {
    this.videoId = videoId;
  }


  public String getVideoName() {
    return videoName;
  }

  public void setVideoName(String videoName) {
    this.videoName = videoName;
  }


  public String getVideoAdreess() {
    return videoAdreess;
  }

  public void setVideoAdreess(String videoAdreess) {
    this.videoAdreess = videoAdreess;
  }


  public int getVideoConsume() {
    return videoConsume;
  }

  public void setVideoConsume(int videoConsume) {
    this.videoConsume = videoConsume;
  }


  public String getVideoType() {
    return videoType;
  }

  public void setVideoType(String videoType) {
    this.videoType = videoType;
  }

}
